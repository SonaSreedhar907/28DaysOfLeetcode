// reverse string

// Example 1:


// Input: s = ["h","e","l","l","o"]
// Output: ["o","l","l","e","h"]
// Example 2:

// Input: s = ["H","a","n","n","a","h"]
// Output: ["h","a","n","n","a","H"]


function reverse(input){
   const b = input.reverse()
   return b
}
var input = ["h","e","l","l","o"]
console.log(reverse(input))